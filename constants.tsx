export const getStatusColor = (status: InterventionStatus): string => {
  switch (status) {
    case InterventionStatus.TODO:
      return "bg-blue-500 text-white";
    case InterventionStatus.IN_PROGRESS:
      return "bg-yellow-500 text-black";
    case InterventionStatus.COMPLETED:
      return "bg-green-500 text-white";
    case InterventionStatus.CANCELLED:
      return "bg-red-500 text-white";
    default:
      return "bg-gray-500 text-white";
  }
};

export const getPriorityColor = (priority: InterventionPriority): string => {
  switch (priority) {
    case InterventionPriority.LOW:
      return "bg-green-100 text-green-800";
    case InterventionPriority.MEDIUM:
      return "bg-yellow-100 text-yellow-800";
    case InterventionPriority.HIGH:
      return "bg-orange-100 text-orange-800";
    case InterventionPriority.URGENT:
      return "bg-red-100 text-red-800";
    default:
      return "bg-gray-100 text-gray-800";
  }
};

